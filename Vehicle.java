package JavaConcepts;

/* Vehicle is parent class or Super Class,  it lists same features that can be used in both child classes */

public class Vehicle {

    int frame = 1;
    String radiator = "Blue Radiator"; // standard color of radiator for vehicles


    public void drive() { // standard speed for all vehicles
        System.out.println("100 KM Per hour");
    } // standard speed for every vehicle

    public void honkhorn() {
        System.out.println("Beep Beep"); // standard horn sound for all vehicles

    }

}

















